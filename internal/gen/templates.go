package gen

// tmplField is a create/update/apply projection of a [Field].
type tmplField struct {
	GoName   string
	JSONName string
	Setter   string // tfkit setter helper name, e.g. "SetString"
	Getter   string // tfkit getter helper name, e.g. "GetString"
}

// tmplResource is the per-resource rendering view.
type tmplResource struct {
	GoName          string
	TFName          string
	ResourceType    string
	ModelType       string
	SchemaFunc      string
	StructName      string
	ConstructorName string
	CollectionConst string
	CollectionPath  string
	Key             string
	KeyGoName       string
	HasCreate       bool
	HasRead         bool
	HasUpdate       bool
	HasDelete       bool
	NeedApply       bool
	CreateFields    []tmplField
	UpdateFields    []tmplField
	ApplyFields     []tmplField
}

// tmplModel is the full template input.
type tmplModel struct {
	Package    string
	Provider   string
	Module     string
	NeedURL    bool
	NeedString bool
	Resources  []tmplResource
}

// glueTmpl renders the per-resource CRUD glue: a resource.Resource impl that
// reuses the generated schema, injects the tfkit client, and maps the model to
